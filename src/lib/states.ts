// Reverse lookup: full name → state code
export function stateCodeFromName(name: string): string {
  const entry = Object.entries(STATE_NAMES).find(
    ([, v]) => v.toLowerCase() === name.toLowerCase()
  );
  return entry ? entry[0] : name;
}
